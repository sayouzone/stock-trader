class StrategyAdvisor:
    """레짐 기반 전략 추천기"""

    def recommend(self, regime_result: RegimeResult) -> list[StrategyFit]:
        """현재 레짐에 맞는 전략 추천 (적합도 순 정렬)"""
        fits = REGIME_STRATEGY_MAP.get(regime_result.regime, [])
        return sorted(fits, key=lambda f: f.score, reverse=True)

    def format_recommendation(self, regime_result: RegimeResult) -> str:
        """추천 결과를 포맷팅된 문자열로 반환"""
        fits = self.recommend(regime_result)
        lines = [
            f"\n{'═' * 60}",
            f"  📊 시장 레짐 분석 결과",
            f"{'═' * 60}",
            regime_result.summary(),
            f"\n{'─' * 60}",
            f"  📡 시그널 상세",
            f"{'─' * 60}",
        ]

        for s in regime_result.signals:
            icon = "🟢" if s.regime_score > 0.5 else "🔴" if s.regime_score < -0.5 else "🟡"
            lines.append(f"  {icon} {s.name:16s} │ {s.description}")

        lines.extend([
            f"\n{'═' * 60}",
            f"  🎯 전략 추천 ({regime_result.regime.label})",
            f"{'═' * 60}",
        ])

        for fit in fits:
            lines.append(f"\n  {fit.label}  {fit.name}")
            lines.append(f"    {fit.reason}")
            lines.append(f"    핵심 행동:")
            for action in fit.key_actions:
                lines.append(f"      → {action}")

        # 리스크 관리 경고
        lines.extend([
            f"\n{'─' * 60}",
            f"  ⚠️  리스크 관리 규칙 (문서 기반)",
            f"{'─' * 60}",
            f"  • 3연패 → 포지션 사이즈 50% 축소",
            f"  • 5연패 → 매매 중단, 전략 점검",
            f"  • 월간 손실 -6% → 해당 월 매매 중단",
            f"  • 포지션 사이징: (계좌 × 리스크%) ÷ (진입가 - 손절가)",
        ])

        return "\n".join(lines)
